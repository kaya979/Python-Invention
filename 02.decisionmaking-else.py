# Else is often used with an if statement
# After an if statement comes back as false, the else is evaluated
#
#  E.G.
# if condition :
#     statements if True

# else: 
#     statements if False

########################################### if True do the next code block: 

if 5 == 5 :
    print("You successfully learned the if statement!")

else: 
    print ("WOW! You also learned about else statement.")

########################################### if False skip me and go to the next line ==> else:
if 5 == 1 :
    print("You successfully learned the if statement!")

else: 
    print ("WOW! You also learned about else statement.")


